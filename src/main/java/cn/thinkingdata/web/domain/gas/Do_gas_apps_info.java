package cn.thinkingdata.web.domain.gas;


public class Do_gas_apps_info {

	private Integer type;
	private String app_name;
	private String app_id;
	private String app_author;
	private String app_img;
	private String app_href;
	private String app_author_href;
	private String app_type;

	public Integer getType() {
		return type;
	}

	public void setType(Integer type) {
		this.type = type;
	}

	public String getApp_name() {
		return app_name;
	}

	public void setApp_name(String app_name) {
		this.app_name = app_name;
	}

	public String getApp_id() {
		return app_id;
	}

	public void setApp_id(String app_id) {
		this.app_id = app_id;
	}

	public String getApp_author() {
		return app_author;
	}

	public void setApp_author(String app_author) {
		this.app_author = app_author;
	}

	public String getApp_img() {
		return app_img;
	}

	public void setApp_img(String app_img) {
		this.app_img = app_img;
	}

	public String getApp_href() {
		return app_href;
	}

	public void setApp_href(String app_href) {
		this.app_href = app_href;
	}

	public String getApp_author_href() {
		return app_author_href;
	}

	public void setApp_author_href(String app_author_href) {
		this.app_author_href = app_author_href;
	}

	public String getApp_type() {
		return app_type;
	}

	public void setApp_type(String app_type) {
		this.app_type = app_type;
	}
}
